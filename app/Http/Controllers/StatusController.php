     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $this->validate($request, [
            'name' => 'required',
            'color' => 'required',
        ]);
        Status::create($data);
        session()->flash('success');
        return redirect()->route('status.index');
    }

